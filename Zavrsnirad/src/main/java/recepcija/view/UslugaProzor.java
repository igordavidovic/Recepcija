    private void ucitaj() {
        DefaultListModel<Usluga> m = new DefaultListModel<>();
        List<Usluga> usluge = ou.read();
        for (Usluga u : usluge) {
            m.addElement(u);
        }
        lstUsluge.setModel(m);    }
    
    private void vrijednosti() {
        var s = ou.getEntitet();
        s.setNaziv(txtNaziv.getText());
        try {
            s.setCijena(new BigDecimal(df.parse(txtCijena.getText()).toString()));
        } catch (Exception e) {
            s.setCijena(BigDecimal.ZERO);
        }
    }
